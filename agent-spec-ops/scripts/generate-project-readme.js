#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");
const { appendEvent } = require("./lib/memory-store");

const args = parseArgs(process.argv.slice(2));

if (!args.stateFile) {
  console.error([
    "Usage: node scripts/generate-project-readme.js runs/<DELIVERY_ID>/workflow-state.json [options]",
    "",
    "Options:",
    "  --project-repo PATH   Absolute path to the project repo (required)",
    "  --readme-title TITLE  Project title (default: delivery title)",
    "  --description TEXT    Short project description (default: request_summary)",
    "  --force               Overwrite existing README.md without prompt"
  ].join("\n"));
  process.exit(1);
}

if (!args.projectRepo) {
  console.error("--project-repo is required. Specify the absolute path to the project repo.");
  process.exit(1);
}

const statePath = path.resolve(args.stateFile);
const state = JSON.parse(fs.readFileSync(statePath, "utf8"));

const deliveryId = state.delivery && state.delivery.id ? state.delivery.id : path.basename(path.dirname(statePath));
const title = args.readmeTitle || (state.delivery && state.delivery.title ? state.delivery.title : deliveryId);
const description = args.description || (state.delivery && state.delivery.request_summary ? state.delivery.request_summary : "");
const tasks = state.task_graph && Array.isArray(state.task_graph.tasks) ? state.task_graph.tasks : [];
const repoPath = path.resolve(args.projectRepo);
const readmePath = path.join(repoPath, "README.md");

if (fs.existsSync(readmePath) && !args.force) {
  console.log(`README.md already exists at ${readmePath}. Use --force to overwrite.`);
  process.exit(0);
}

const frontendTasks = tasks.filter((t) => ["frontend_dev", "frontend_test"].includes(t.role));
const backendTasks = tasks.filter((t) => ["backend_dev", "backend_test"].includes(t.role));
const verified = tasks.filter((t) => t.status === "verified");
const blocked = tasks.filter((t) => t.status === "blocked");

const lines = [];

lines.push(`# ${title}`);
lines.push(``);

if (description) {
  lines.push(description);
  lines.push(``);
}

lines.push(`## Overview`);
lines.push(``);
lines.push(`This project uses the **agent-spec-ops** harness to manage its`);
lines.push(`full spec-to-delivery lifecycle. All delivery runs are managed`);
lines.push(`through the harness and tracked in \`runs/<DELIVERY_ID>/\`.`);
lines.push(``);

if (tasks.length) {
  lines.push(`## Current Delivery: ${deliveryId}`);
  lines.push(``);
  lines.push(`| Metric | Value |`);
  lines.push(`| --- | --- |`);
  lines.push(`| Total tasks | ${tasks.length} |`);
  lines.push(`| Verified | ${verified.length} |`);
  lines.push(`| Blocked | ${blocked.length} |`);
  lines.push(`| Frontend tasks | ${frontendTasks.length} |`);
  lines.push(`| Backend tasks | ${backendTasks.length} |`);
  lines.push(`| Updated | ${state.delivery && state.delivery.updated_at ? state.delivery.updated_at : "n/a"} |`);
  lines.push(``);

  lines.push(`### Tasks`);
  lines.push(``);
  lines.push(`| ID | Title | Role | Status |`);
  lines.push(`| --- | --- | --- | --- |`);
  for (const task of tasks) {
    lines.push(`| ${task.id} | ${task.title || "Untitled"} | ${task.role} | ${task.status} |`);
  }
  lines.push(``);
}

lines.push(`## Development Setup`);
lines.push(``);
lines.push(`### Prerequisites`);
lines.push(``);
lines.push(`- Node.js 18+`);
lines.push(`- Docker (for integration testing)`);
if (frontendTasks.length) {
  lines.push(`- A frontend package manager (npm / yarn / pnpm)`);
}
if (backendTasks.length) {
  lines.push(`- Backend runtime (Node.js / Python / Go / etc.)`);
}
lines.push(``);

lines.push(`### Getting Started`);
lines.push(``);
lines.push(`1. Clone the repository`);
lines.push(`2. Install dependencies: \`npm install\``);
lines.push(`3. Run the development server: \`npm run dev\``);
lines.push(`4. Run tests: \`npm test\``);
lines.push(``);

lines.push(`### Running with the Harness`);
lines.push(``);
lines.push(`This project is managed by the \`agent-spec-ops\` harness. To start`);
lines.push(`a new delivery session:`);
lines.push(``);
lines.push(`\`\`\`bash`);
lines.push(`cd agent-spec-ops`);
lines.push(`node scripts/read-context.js runs/${deliveryId}/workflow-state.json`);
lines.push(`\`\`\``);
lines.push(``);

lines.push(`## Architecture`);
lines.push(``);
if (frontendTasks.length) {
  lines.push(`- **Frontend**: Located in the frontend directory`);
}
if (backendTasks.length) {
  lines.push(`- **Backend**: Located in the backend directory`);
}
lines.push(`- **Harness**: \`agent-spec-ops/\` contains the delivery management system`);
lines.push(``);

lines.push(`---`);
lines.push(`*Generated by agent-spec-ops harness at ${new Date().toISOString()}*`);

fs.mkdirSync(path.dirname(readmePath), { recursive: true });
fs.writeFileSync(readmePath, lines.join("\n") + "\n");
console.log(`Generated project README: ${readmePath}`);

appendEvent(statePath, {
  type: "artifact_generated",
  role_context: "orchestrator",
  task_id: "",
  target: "project-README.md",
  summary: `Generated project README.md at ${readmePath}`,
  details: `Wrote ${lines.length} lines`,
  severity: "info",
  tags: ["artifact", "project-readme", deliveryId]
});

function parseArgs(rawArgs) {
  const parsed = {
    stateFile: "",
    projectRepo: "",
    readmeTitle: "",
    description: "",
    force: false
  };
  for (let index = 0; index < rawArgs.length; index += 1) {
    const arg = rawArgs[index];
    if (!arg.startsWith("--") && !parsed.stateFile) {
      parsed.stateFile = arg;
      continue;
    }
    const value = rawArgs[index + 1] || "";
    switch (arg) {
      case "--project-repo":
        parsed.projectRepo = value;
        index += 1;
        break;
      case "--readme-title":
        parsed.readmeTitle = value;
        index += 1;
        break;
      case "--description":
        parsed.description = value;
        index += 1;
        break;
      case "--force":
        parsed.force = true;
        break;
    }
  }
  return parsed;
}
